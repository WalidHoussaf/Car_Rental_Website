// Helper function to resolve nested image references
export const resolveImagePaths = (dataArray, imageField) => {
    return dataArray.map(item => {
      const path = item[imageField].split('.');
      let resolved = assets;
      
      path.forEach(key => {
        resolved = resolved[key];
      });
      
      return { 
        ...item, 
        [imageField]: resolved 
      };
    });
};

// Helper function to resolve gallery image paths
export const resolveGalleryPaths = (dataArray) => {
  return dataArray.map(item => {
      // Skip if car has no id or we don't have gallery for it
      if (!item.id || !assets.cars.gallery[`car${item.id}`]) {
          return item;
      }
      
      // Resolve main image
      const imagePath = item.image.split('.');
      let resolvedImage = assets;
      imagePath.forEach(key => {
          resolvedImage = resolvedImage[key];
      });
      
      // Add gallery images
      const galleryImages = assets.cars.gallery[`car${item.id}`].map((img, index) => ({
          path: img,
          alt: `${item.name} view ${index + 1}`
      }));
      
      return {
          ...item,
          image: resolvedImage,
          gallery: galleryImages
      };
  });
};

